sub renderiscii {
    local ($isciitxt, $isciigif);
    $isciitxt = $_[0];
    $isciitxt1 = $isciitxt;
    $isciitxt =~ s/\357B//;
    while (length($isciitxt) gt 0) {
	$isciitxt =~ s/^(.)//;
	$isciichar = $1;
	$isciiord = ord($isciichar);
	$isciidec = sprintf ("%d",$isciiord); 
	if ($isciidec gt 160 && $isciidec lt 234) {
		$isciiimage = $iscdev2utf{$isciidec};
		$isciigif .= $isciiimage;
		}
	else {
        	$isciigif .= $isciichar ."[". $isciidec . "]";
	        }
	}

return $isciigif;
}


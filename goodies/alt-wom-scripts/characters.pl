sub readnavbar {
        local ($navigbar, $navin, $mvosysdir, $chartext);
        $mvosysdir = $SYSTEM_DIR;
        if (!$mvosysdir) {
                $mvosysdir = $sys_dir;
                }
        open (NAVBAR, $mvosysdir . "lib/Results_Header.html");
        while ($navin = <NAVBAR>) {
                $navin =~ s/EFTSSEARCHFORM/$Search_Form/;
                $navin =~ s/EFTSDATABASEHOME/$eftsdatabasehome/g;
                $navigbar .= $navin;
                }
        close NAVBAR;

        return $navigbar;
}


sub utf2rom() {
	$text = $_[0];
	while(($key, $value) = each(%sgml2utf)) {
	    if ($value !~ /^[A-Za-z]+$/) {
		if ($text =~ /$value/) {
			if ($key =~/\&cap/) {
			    $roman = substr($key, 4, 1);
			} else {
				$roman = substr($key, 1, 1);
			}
		    $text =~ s/$value/$roman/g;
		}
	    }
	}
	return $text;
}


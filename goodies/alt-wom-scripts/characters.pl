sub volumeNum {
    local ($doc) = $_[0];
    local ($num);

# In some reference databases we split our volumes in separate 
# files so that they could be handled easier; for example, in the 
# Encyc. first 17 volumes (that is, all volumes except the ones containing
# plates) are split in 1M files each. So the following function is 
# used to obtain the real number of the volume from the TOMS file number:

#    $num = $doc < 136 ? int ($doc / 8) + 1 : $doc - 118;

    $num = $doc + 1;

    return $num;
}

